class GroupModel{
    setId(id){
        this.id = id;
    }

    setDescription(desc){
        this.desc = desc;
    }

    setColor(color){
        this.color = color;
    }

    setOrder(order){
        this.order = order;
    }

    setMaxQuantity(maxQuantity){
        this.maxQuantity = maxQuantity;
    }
}

export default GroupModel;